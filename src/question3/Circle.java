public class Circle {
    
    private int number;

    public Circle() {
    }

    public Circle(int number) {
        this.number = number;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    @Override
    public String toString() {
        return "Circle{" + "number=" + number + '}';
    }
    
    
}
